class Book {
  final String bookId;
  final String title;
  final String imageUrl;
  final String url;
  final int numPages;
  final int ratingsCount;
  final String description;
  final String genre;
  final String name;
  final int bookIdMapping;

  Book({
    required this.bookId,
    required this.title,
    required this.imageUrl,
    required this.url,
    required this.numPages,
    required this.ratingsCount,
    required this.description,
    required this.genre,
    required this.name,
    required this.bookIdMapping,
  });
}
