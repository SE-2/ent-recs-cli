class Movie {
  final String posterLink;
  final String seriesTitle;
  final int releasedYear;
  final String certificate;
  final String runtime;
  final String genre;
  final double imdbRating;
  final String overview;
  final int metaScore;
  final String director;
  final String star1;
  final String star2;
  final String star3;
  final String star4;
  final int numOfVotes;
  final String gross;

  Movie({
    required this.posterLink,
    required this.seriesTitle,
    required this.releasedYear,
    required this.certificate,
    required this.runtime,
    required this.genre,
    required this.imdbRating,
    required this.overview,
    required this.metaScore,
    required this.director,
    required this.star1,
    required this.star2,
    required this.star3,
    required this.star4,
    required this.numOfVotes,
    required this.gross,
  });
}